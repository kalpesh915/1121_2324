/**
    relational operator ( != )

    true    =   1
    false   =   0
*/
#include<stdio.h>

void main(){
    printf("\n answer is %d", (10 != 10));
    printf("\n answer is %d", (10 != 100));
    printf("\n answer is %d", (111 != 111));
    printf("\n answer is %d", (111 != 101));
    printf("\n answer is %d", (51 != 15));
}
