/**
    relational operator ( >, >= )

    true    =   1
    false   =   0
*/
#include<stdio.h>

void main(){
    printf("\n answer is %d", (51 > 15));
    printf("\n answer is %d", (51 > 115));
    printf("\n answer is %d", (151 > 115));
    printf("\n answer is %d", (511 > 515));
    printf("\n answer is %d", (51 > 51));
    printf("\n answer is %d", (51 >= 51));
}
