#include<stdio.h>

/**
    Assignment operators
*/

void main(){
    int ip = 10;
    printf("\n value of ip is %d", ip);
    ip = ip + 10;
    printf("\n value of ip is %d", ip);
    ip += 10;
    printf("\n value of ip is %d", ip);
    ip -= 10;
    printf("\n value of ip is %d", ip);
    ip *= 10;
    printf("\n value of ip is %d", ip);
    ip /= 10;
    printf("\n value of ip is %d", ip);
    ip %= 10;
    printf("\n value of ip is %d", ip);
}
